class Window_KanaInput < Window_NameInputBase
  #--------------------------------------------------------------------------
  # ● 定数（各操作説明文）
  #--------------------------------------------------------------------------
  lr = "\\C[14]L R \\C[0]"
  lr_short = lr + "切替"
  lr_long  = lr + "姓名入力切り替え"

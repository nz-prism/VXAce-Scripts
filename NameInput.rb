class Window_NameInputBase < Window_Selectable
  #--------------------------------------------------------------------------
  # ● 公開インスタンス変数
  #--------------------------------------------------------------------------
  attr_writer   :edit_window
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize(x, y, width, height, edit_window)
    @page = 0
    super(x, y, width, height)
    @edit_window = edit_window
    @index = 0
    refresh
    update_cursor
    deactivate
    unselect
  end
  #--------------------------------------------------------------------------
  # ● 文字の取得
  #--------------------------------------------------------------------------
  def character
    table[@page][@index]
  end
  #--------------------------------------------------------------------------
  # ● 項目数の取得
  #--------------------------------------------------------------------------
  def item_max
    table[@page].size
  end
  #--------------------------------------------------------------------------
  # ● 項目の選択解除
  #--------------------------------------------------------------------------
  def unselect
    @last_index = @index
    super
  end
  #--------------------------------------------------------------------------
  # ● 最後に選んだ項目を選択
  #--------------------------------------------------------------------------
  def select_last
    select((@last_index >= item_max) ? 0 : @last_index)
  end
  #--------------------------------------------------------------------------
  # ● 項目の描画
  #--------------------------------------------------------------------------
  def draw_item(index)
    draw_text(item_rect(index), table[@page][index], 1)
  end
  #--------------------------------------------------------------------------
  # ● ページ変更
  #--------------------------------------------------------------------------
  def change_page(page)
    @page = page
    refresh
  end
  #--------------------------------------------------------------------------
  # ● 名前に文字を追加
  #--------------------------------------------------------------------------
  def on_name_add
    if @edit_window.add(character)
      Sound.play_ok
    else
      Sound.play_buzzer
    end
  end
  #--------------------------------------------------------------------------
  # ● 決定ボタンが押されたときの処理
  #--------------------------------------------------------------------------
  def process_ok
    if current_item_enabled?
      Sound.play_ok
      Input.update
      call_ok_handler
    else
      Sound.play_buzzer
    end
  end
  #--------------------------------------------------------------------------
  # ● カーソルを下に移動
  #     wrap : ラップアラウンド許可
  #--------------------------------------------------------------------------
  def cursor_down(wrap = false)
    if row_max > 1
      n = @index + col_max
      if n > item_max - 1
        if wrap
          select(n % col_max)
        end
      else
        select(n)
      end
    end
  end
  #--------------------------------------------------------------------------
  # ● カーソルを上に移動
  #     wrap : ラップアラウンド許可
  #--------------------------------------------------------------------------
  def cursor_up(wrap = false)
    if row_max > 1
      n = @index - col_max
      if n < 0
        if wrap
          n = (row_max - 1) * col_max + @index
          if n > item_max - 1
            select(n - col_max)
          else
            select(n)
          end
        end
      else
        select(n)
      end
    end
  end
  #--------------------------------------------------------------------------
  # ● カーソルを右に移動
  #     wrap : ラップアラウンド許可
  #--------------------------------------------------------------------------
  def cursor_right(wrap = false)
    if item_max > 1
      n = col_max - 1
      if @index % col_max < n
        if @index == item_max - 1
          select((row_max - 1) * col_max) if wrap
        else
          select(@index + 1)
        end
      elsif wrap
        select(@index - n)
      end
    end
  end
  #--------------------------------------------------------------------------
  # ● カーソルを左に移動
  #     wrap : ラップアラウンド許可
  #--------------------------------------------------------------------------
  def cursor_left(wrap = false)
    if item_max > 1
      if @index % col_max > 0
        select(@index - 1)
      elsif wrap
        select([@index + col_max - 1, item_max - 1].min)
      end
    end
  end
end

